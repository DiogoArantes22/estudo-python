""""Aula 03 - Comentários em Python"""

# Comentários são trechos de texto no código que são ignorados pelo interpretador
# Eles servem para explicar o código, tornando-o mais fácil de entender

# Comentário de uma linha começa com o símbolo #
'''
Comentários de múltiplas linhas podem ser feitos usando três aspas simples ou duplas
Isso é útil para explicar trechos maiores de código ou fornecer documentação'''

n1 = 10  # primeira nota
n2 = 8   # segunda nota
n3 = 9   # terceira nota

print("Notas:", n1, n2)
print("Terceira nota:", n3)

# boa prática: usar comentários para explicar o "porquê" do código, não o "o quê"
# Exemplo ruim:
x = +  1  # incrementa x em 1
# Exemplo bom:
x = x + 1  # incrementa x para contar o número de tentativas
