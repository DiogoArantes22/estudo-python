"""Aula 04 - while loop"""
# while loop é usado para repetir um bloco de código enquanto uma condição for verdadeira

contador = 0
while contador <= 10:
    print('Contador:', contador)
    contador += 1  # Incrementa o contador em 1
    
print('Fim')

